package assignment_3;

public class Question4_SwapNumbers {

	public static void main(String[] args) {

		int num1, num2, num3;
		
		num1= 10;
		num2=20;
		num3=40;
				
		System.out.println("n1="+num1);
		System.out.println("n2="+num2);
		
		System.out.println("--------");
		
		num3=num1;
		num1= num2;
		num2=num3;
		
		System.out.println("n1="+num1);
		System.out.println("n2="+num2);
		
	}

}
