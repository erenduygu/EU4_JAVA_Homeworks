package assignment_6_day11;

public class Question4_1and0Triangle {

	public static void main(String[] args) {

		for (int i = 1; i <= 7; i++) {
			for (int j = 1; j <= i; j++) {
				if (j % 2 == 1)
					System.out.print(j % 2);
				else
					System.out.print(j % 2);
			}
			System.out.println(" ");

		}
	}

}
